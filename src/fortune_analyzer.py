class FortuneAnalyzer:
    def analyze(self, emojis: list[str]) -> tuple[str, str]:
        if not emojis:
            return ("🔮", FORTUNE_MESSAGES["その他"])

        counter = Counter(emojis)
        top_emoji, _ = counter.most_common(1)[0]

        for category, emoji_list in EMOJI_CATEGORIES.items():
            if top_emoji in emoji_list:
                return (top_emoji, FORTUNE_MESSAGES[category])

        return (top_emoji, FORTUNE_MESSAGES["その他"])
